#include <stdio.h>
#include <fcntl.h>
#include <unistd.h>

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

int	main(void)
{
	int	fd;

	fd = open("deneme", O_RDWR | O_CREAT, 0666);
	ft_putchar_fd('a', fd);
}
